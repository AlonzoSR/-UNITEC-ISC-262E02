using System.Security.Cryptography.X509Certificates;

public class Rectangulo262E02 {
public static double Perimetro(double lado_a, double lado_b) {
return 2*lado_a + 2*lado_b;
}
public static double Area(double lado_a, double lado_b) {
return lado_a * lado_b;
}
public static double Volumen(double area, double altura) {
return area * altura;
}
}